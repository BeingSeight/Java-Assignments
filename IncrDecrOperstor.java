// Increment Decrement Operator

public class IncrDecrOperstor {
    public static void main(String[] args) {
        int num1 = 80;
        int num2 = 240;
        num1++; // num1 = num1 + 1
        num2--; // num2 = num2 - 1
        System.out.println("num1: " + num1);
        System.out.println("num2: " + num2);
    }
}