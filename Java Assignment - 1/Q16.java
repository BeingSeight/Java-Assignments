// 16. WAP to demonstrate relational operators in java.

class Q16 {
    public static void main(String[] args) {
        int a = 5;
        int b = 3;

        System.out.println("a == b: " + (a == b));
        System.out.println("a != b: " + (a != b));
        System.out.println("a > b: " + (a > b)); 
        System.out.println("a < b: " + (a < b)); 
        System.out.println("a >= b: " + (a >= b)); 
        System.out.println("a <= b: " + (a <= b)); 
    }
    
}

