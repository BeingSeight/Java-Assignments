// 25. WAP to find whether the inputted number is even or odd.

public class Q25 {
    public static void main(String[] args) {
        int num = 10;

        if (num % 2 == 0) {
            System.out.println(num + " is even");
        } else {
            System.out.println(num + " is odd");
        }
    }
}

